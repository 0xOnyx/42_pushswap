#include "push_swap.h"

int	number(char c)
{
	if ((c < '0' || c > '9') && c != '-')
		return (0);
	return (1);
}

int	is_number(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i] < '0' || str[i] > '9') && str[i] != '-')
			return (0);
		i++;
	}
	return (1);
}
